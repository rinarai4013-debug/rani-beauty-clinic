/**
 * Get all protocols
 */
export function getAllProtocols(): TreatmentProtocol[] {
  return PROTOCOLS;
}

/**
 * Get protocol by ID
 */
export function getProtocolById(id: string): TreatmentProtocol | undefined {
  return PROTOCOLS.find(p => p.id === id);
}

/**
 * Get protocols by category
 */
export function getProtocolsByCategory(category: TreatmentCategory): TreatmentProtocol[] {
  return PROTOCOLS.filter(p => p.category === category);
}

/**
 * Get protocols matching a set of skin concerns
 */
export function getProtocolsForConcerns(concerns: string[]): TreatmentProtocol[] {
  return PROTOCOLS.filter(p =>
    p.indication.some(ind =>
      concerns.some(c => ind.toLowerCase().includes(c.toLowerCase()))
    )
  );
}

