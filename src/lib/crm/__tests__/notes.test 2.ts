import {
  createNote,
  createAutoNote,
  togglePin,
  updateNote,
  addMention,
  getTemplate,
  renderTemplate,
  buildClientTimeline,
  filterNotes,
  getActivitySummary,
  NOTE_TEMPLATES,
} from '../notes';
import type { CRMNote, NoteFilter, NoteTemplate } from '@/types/crm';

function makeNote(overrides: Partial<CRMNote> = {}): CRMNote {
  return {
    id: 'note_test',
    clientId: 'c_test',
    clientName: 'Test Client',
    type: 'call',
    subject: 'Test note',
    content: 'Test content',
    createdBy: 'Rina',
    createdAt: '2026-03-25T14:00:00Z',
    updatedAt: '2026-03-25T14:00:00Z',
    isPinned: false,
    mentions: [],
    tags: [],
    attachments: [],
    isAutoGenerated: false,
    ...overrides,
  };
}

// ─── Note Creation Tests ─────────────────────────────────────

describe('Notes - Creation', () => {
  test('should create note with required fields', () => {
    const note = createNote({
      clientId: 'c001',
      clientName: 'Sarah',
      type: 'call',
      subject: 'Follow-up call',
      content: 'Discussed treatment options',
      createdBy: 'Rina',
    });
    expect(note.id).toMatch(/^note_/);
    expect(note.clientId).toBe('c001');
    expect(note.type).toBe('call');
    expect(note.isAutoGenerated).toBe(false);
  });

  test('should create note with tags and mentions', () => {
    const note = createNote({
      clientId: 'c001',
      clientName: 'Sarah',
      type: 'internal',
      subject: 'Team note',
      content: 'Review this client',
      createdBy: 'Rina',
      tags: ['priority', 'vip'],
      mentions: ['Mom'],
    });
    expect(note.tags).toEqual(['priority', 'vip']);
    expect(note.mentions).toEqual(['Mom']);
  });

  test('should create pinned note', () => {
    const note = createNote({
      clientId: 'c001',
      clientName: 'Sarah',
      type: 'internal',
      subject: 'Important',
      content: 'VIP client preferences',
      createdBy: 'Rina',
      isPinned: true,
    });
    expect(note.isPinned).toBe(true);
  });

  test('should create auto-generated note', () => {
    const note = createAutoNote('c001', 'Sarah', 'Stage Changed', 'Moved to Consulted', 'auto_002');
    expect(note.isAutoGenerated).toBe(true);
    expect(note.createdBy).toBe('system');
    expect(note.automationId).toBe('auto_002');
    expect(note.tags).toContain('auto_generated');
  });

  test('should set timestamps on creation', () => {
    const note = createNote({
      clientId: 'c001', clientName: 'Sarah', type: 'call',
      subject: 'Test', content: 'Test', createdBy: 'Rina',
    });
    expect(note.createdAt).toBeDefined();
    expect(note.updatedAt).toBeDefined();
  });
});

// ─── Note Management Tests ───────────────────────────────────

describe('Notes - Management', () => {
  test('should toggle pin', () => {
    const note = makeNote({ isPinned: false });
    const pinned = togglePin(note);
    expect(pinned.isPinned).toBe(true);
    const unpinned = togglePin(pinned);
    expect(unpinned.isPinned).toBe(false);
  });

  test('should update note content', () => {
    const note = makeNote();
    const updated = updateNote(note, { subject: 'Updated subject', content: 'Updated content' });
    expect(updated.subject).toBe('Updated subject');
    expect(updated.content).toBe('Updated content');
    expect(updated.updatedAt).not.toBe(note.updatedAt);
  });

  test('should update note tags', () => {
    const note = makeNote({ tags: ['old'] });
    const updated = updateNote(note, { tags: ['new', 'tag'] });
    expect(updated.tags).toEqual(['new', 'tag']);
  });

  test('should add mention', () => {
    const note = makeNote({ mentions: [] });
    const withMention = addMention(note, 'Mom');
    expect(withMention.mentions).toContain('Mom');
  });

  test('should not duplicate mentions', () => {
    const note = makeNote({ mentions: ['Rina'] });
    const result = addMention(note, 'Rina');
    expect(result.mentions).toHaveLength(1);
  });
});

