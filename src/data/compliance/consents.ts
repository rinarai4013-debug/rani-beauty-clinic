export function getConsentTemplatesByCategory(
  category: ConsentTemplate['treatmentCategory']
): ConsentTemplate[] {
  return CONSENT_TEMPLATES.filter(
    (t) => t.treatmentCategory === category && t.status === 'active'
  );
}

export function getConsentCategories(): {
  category: ConsentTemplate['treatmentCategory'];
  label: string;
  count: number;
}[] {
  const categories: {
    category: ConsentTemplate['treatmentCategory'];
    label: string;
  }[] = [
    { category: 'injectable', label: 'Injectables' },
    { category: 'laser', label: 'Laser & Energy' },
    { category: 'skin', label: 'Skin Treatments' },
    { category: 'wellness', label: 'Wellness' },
    { category: 'body', label: 'Body' },
    { category: 'photography', label: 'Photography' },
    { category: 'consultation', label: 'Consultation' },
  ];

  return categories.map((c) => ({
    ...c,
    count: CONSENT_TEMPLATES.filter(
      (t) => t.treatmentCategory === c.category && t.status === 'active'
    ).length,
  }));
}
