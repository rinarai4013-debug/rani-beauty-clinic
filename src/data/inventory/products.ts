export function getDaysUntilReorder(product: Product, avgDailyUsage: number): number | null {
  if (avgDailyUsage <= 0) return null;
  const unitsAboveReorder = product.currentStock - product.reorderPoint;
  if (unitsAboveReorder <= 0) return 0;
  return Math.floor(unitsAboveReorder / avgDailyUsage);
}

export function getExpiryRisk(expirationDate: Date): 'expired' | 'critical' | 'warning' | 'ok' {
  const now = new Date();
  const daysUntilExpiry = Math.floor((expirationDate.getTime() - now.getTime()) / (1000 * 60 * 60 * 24));
  if (daysUntilExpiry <= 0) return 'expired';
  if (daysUntilExpiry <= 30) return 'critical';
  if (daysUntilExpiry <= 90) return 'warning';
  return 'ok';
}

export function calculateBudExpiry(reconstitutionTime: Date, budHours: number): Date {
  return new Date(reconstitutionTime.getTime() + budHours * 60 * 60 * 1000);
}

export function getTotalInventoryValue(): number {
  return products.reduce((sum, p) => sum + p.currentStock * p.unitCost, 0);
}

export function getTotalRetailValue(): number {
  return products.filter((p) => p.retailPrice > 0).reduce((sum, p) => sum + p.currentStock * p.retailPrice, 0);
}

/** Get count of products at or below reorder point */
export function getReorderCount(): number {
  return products.filter((p) => p.active && p.currentStock <= p.reorderPoint).length;
}
