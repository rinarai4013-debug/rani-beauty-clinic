export function getProductById(id: string): Product | undefined {
  return products.find((p) => p.id === id);
}

export function getProductsByCategory(category: ProductCategory): Product[] {
  return products.filter((p) => p.category === category && p.active);
}

export function getProductsBySupplier(supplierId: string): Product[] {
  return products.filter((p) => p.supplier === supplierId && p.active);
}

export type StockStatus = 'full' | 'adequate' | 'low' | 'critical' | 'out';

export function getStockStatus(product: Product): StockStatus {
  const { currentStock, reorderPoint, parLevel } = product;
  if (currentStock === 0) return 'out';
  if (currentStock <= Math.floor(reorderPoint * 0.5)) return 'critical';
  if (currentStock <= reorderPoint) return 'low';
  if (currentStock >= parLevel) return 'full';
  return 'adequate';
}

