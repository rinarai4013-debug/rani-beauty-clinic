// ── Utility Functions ─────────────────────────────────────────────────

export function getSupplierById(id: string): Supplier | undefined {
  return suppliers.find((s) => s.id === id);
}

export function getActiveSuppliers(): Supplier[] {
  return suppliers.filter((s) => s.active);
}

export function getSuppliersByCategory(category: string): Supplier[] {
  return suppliers.filter((s) => s.active && s.category.toLowerCase().includes(category.toLowerCase()));
}

export interface SupplierScorecard {
  supplierId: string;
  supplierName: string;
  onTimeRate: number;
  qualityScore: number;
  avgLeadTime: number;
  totalOrders: number;
  totalSpend: number;
  priceConsistency: number;
  overallScore: number;
}

export function calculateSupplierScore(supplier: Supplier): number {
  const deliveryScore = supplier.onTimeDeliveryRate;
  const qualityPct = (supplier.qualityScore / 5) * 100;
  const leadTimeScore = Math.max(0, 100 - (supplier.leadTimeDays - 2) * 10);

  return Math.round(deliveryScore * 0.4 + qualityPct * 0.35 + leadTimeScore * 0.25);
}
